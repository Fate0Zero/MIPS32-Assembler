#include "Register.h"

#define NUMRECORDS 28

static Register RegisterTable[NUMRECORDS] = {
	{0, "00000", "$zero"},
	{1, "00001", "$at"},
	{2, "00010", "$v0"},
	{3, "00011", "$v1"},
	{4, "00100", "$a0"},
	{5, "00101", "$a1"},
	{6, "00110", "$a2"},
	{7, "00111", "$a3"},
	{8, "01000", "$t0"},
	{9, "01001", "$t1"},
	{10, "01010", "$t2"},
	{11, "01011", "$t3"},
	{12, "01100", "$t4"},
	{13, "01101", "$t5"},
	{14, "01110", "$t6"},
	{15, "01111", "$t7"},
	{16, "10000", "$s0"},
	{17, "10001", "$s1"},
	{18, "10010", "$s2"},
	{19, "10011", "$s3"},
	{20, "10100", "$s4"},
	{21, "10101", "$s5"},
	{22, "10110", "$s6"},
	{23, "10111", "$s7"},
	{24, "11000", "$t8"},
	{25, "11001", "$t9"},
	{26, "11010", "$k0"},
	{27, "11011", "$k1"}
};

const Register* FindR(const char* const reg) {
	for (int i = 0; i < NUMRECORDS; i++) {
		if (strcmp(reg, RegisterTable[i].name) == 0) {
			return &RegisterTable[i];
		}
	}
	return NULL;
}
